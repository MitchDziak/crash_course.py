def make_shirt(size='l', name='i love python'):
    """Display the shirt's description."""
    print("Your size: " + size.upper() + " shirt is being printed with " + name.upper()
        + " on the back.")
        
make_shirt('s', 'koutavi')
make_shirt(name='honeybird', size='m')
make_shirt()
